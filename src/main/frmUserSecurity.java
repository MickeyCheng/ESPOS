    private void CheckCredentials(){
        try{
            dbConn.pstmt = dbConn.conn.prepareStatement("Select * from tblusercontrol where accountName=?");
            dbConn.pstmt.setString(1, cmbUserName.getSelectedItem().toString());
            dbConn.rs = dbConn.pstmt.executeQuery();
            if (dbConn.rs.next()){
                GetCategory = dbConn.rs.getString("position");
                GetAdmin = dbConn.rs.getString("isAdmin");
                GetUserId = dbConn.rs.getString("userName");
            }
            dbConn.pstmt.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void FillComboUsers(){
        cmbUserName.removeAllItems();
        try{
            dbConn.pstmt = dbConn.conn.prepareStatement("Select * from tblusercontrol order by accountName");
            dbConn.rs =dbConn.pstmt.executeQuery();
            while(dbConn.rs.next()){
                cmbUserName.addItem(dbConn.rs.getString("accountName"));
            }
            dbConn.pstmt.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
