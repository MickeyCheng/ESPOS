    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
            CheckBoxCheck();
            try{
                String SQLQuery = "update tblusersecurity set us_product=?,us_supplier=?,us_lpo=?,us_stockadjust=?,us_cashier=?,"
                        + "us_expense=?,us_receipt=?,us_reports=?,us_usercontrol=?,us_usersecurity=?,"
                        + "us_adduser=?,us_date=? where us_name=?";
                dbConn.pstmt = dbConn.conn.prepareStatement(SQLQuery);
                dbConn.pstmt.setString(1, checkProduct);
                dbConn.pstmt.setString(2, checkSupplier);
                dbConn.pstmt.setString(3, checkLpo);
                dbConn.pstmt.setString(4, checkInventory);
                dbConn.pstmt.setString(5, checkSales);
                dbConn.pstmt.setString(6, checkExpense);
                dbConn.pstmt.setString(7, checkReceipt);
                dbConn.pstmt.setString(8, checkReports);
                dbConn.pstmt.setString(9, checkUserControl);
                dbConn.pstmt.setString(10, checkUserSecurity);
                dbConn.pstmt.setString(11,dbConn.LoggedInUser );
                dbConn.pstmt.setString(12, dbConn.sdfDateGlobal.format(dbConn.todayDateGlobal));
                dbConn.pstmt.setString(13, cmbUserName.getSelectedItem().toString());
                dbConn.saveAuditTrail("UPDATED USER ACCESS: " + cmbUserName.getSelectedItem().toString());
                dbConn.pstmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Acccess Level Saved");
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
    }//GEN-LAST:event_btnSaveActionPerformed

